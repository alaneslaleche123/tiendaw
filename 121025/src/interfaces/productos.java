    private void subirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subirActionPerformed
        //Subir
        conectar con = new conectar();
        Connection cn = null;
        PreparedStatement psProducto = null;
        PreparedStatement psInventario = null;
        ResultSet rs = null;

        try {
            cn = con.getConnection();

            // --- Tipo de venta ---
            String tipoVenta = "unidad";
            if (jRadioButton1.isSelected()) {
                tipoVenta = "granel";
            } else if (jRadioButton2.isSelected()) {
                tipoVenta = "paquete";
            }

            boolean inventariado = jRadioButton3.isSelected();

            // --- Obtener el IVA seleccionado ---
            ComboItem itemIva = (ComboItem) ivacombox.getSelectedItem();
            int idIvaSeleccionado = itemIva != null ? itemIva.getId() : 1;

            // --- Obtener la categoría seleccionada ---
            ComboItem itemCategoria = (ComboItem) departamentocombox.getSelectedItem();
            int idCategoriaSeleccionada = itemCategoria != null ? itemCategoria.getId() : 1;

            // --- Insertar producto con IVA y categoría ---
            String sqlProducto = "INSERT INTO productos (codigo_barras, descripcion, precio_compra, precio_venta, ganancia, venta_mayoreo, inventariado, tipo_venta, id_iva, id_categoria) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

            psProducto = cn.prepareStatement(sqlProducto, Statement.RETURN_GENERATED_KEYS);
            psProducto.setString(1, barras.getText());
            psProducto.setString(2, descripcion.getText());
            psProducto.setDouble(3, Double.parseDouble(precioCompra.getText()));
            psProducto.setDouble(4, Double.parseDouble(precioVenta.getText()));
            psProducto.setDouble(5, Double.parseDouble(ganancia.getText()));
            psProducto.setDouble(6, Double.parseDouble(mayoreo.getText()));
            psProducto.setBoolean(7, inventariado);
            psProducto.setString(8, tipoVenta);
            psProducto.setInt(9, idIvaSeleccionado);
            psProducto.setInt(10, idCategoriaSeleccionada);

            int filas = psProducto.executeUpdate();

            if (filas > 0) {
                rs = psProducto.getGeneratedKeys();
                if (rs.next()) {
                    int idProducto = rs.getInt(1);

                    // --- Agregar inventario si aplica ---
                    if (inventariado) {
                        String sqlInventario = "INSERT INTO inventario (id_producto, cantidad, stock_minimo, stock_maximo) VALUES (?, ?, ?, ?)";
                        psInventario = cn.prepareStatement(sqlInventario);
                        psInventario.setInt(1, idProducto);
                        psInventario.setInt(2, Integer.parseInt(cantidad.getText()));
                        psInventario.setInt(3, Integer.parseInt(sminimo.getText()));
                        psInventario.setInt(4, Integer.parseInt(smaximo.getText()));
                        psInventario.executeUpdate();
                    }

                    JOptionPane.showMessageDialog(this, "✅ Producto agregado con IVA y departamento.");
                    mostrarProductos();
                }
            } else {
                JOptionPane.showMessageDialog(this, "❌ Error al agregar el producto.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "⚠️ Error: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (psProducto != null) {
                    psProducto.close();
                }
                if (psInventario != null) {
                    psInventario.close();
                }
                if (cn != null) {
                    cn.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_subirActionPerformed
