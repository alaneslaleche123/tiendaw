    private void mostrarProductos() {
        // Definir columnas
        String[] columnas = {
            "ID", "Código de Barras", "Descripción", "Precio Compra",
            "Precio Venta", "Ganancia", "Categoría", "Venta Mayoreo",
            "Inventariado", "IVA (%)", "Tipo de Venta"
        };

        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(null, columnas) {
            boolean[] canEdit = new boolean[]{
                false, false, true, true,
                true, true, true, true,
                true, true, true
            };

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };

        conecxiones.conectar con = new conecxiones.conectar();
        java.sql.Connection cn = con.getConnection();

        try {
            // Traer nombre_categoria, porcentaje IVA y tipo_venta descriptivo
            String sql = "SELECT p.id_producto, p.codigo_barras, p.descripcion, p.precio_compra, "
                    + "p.precio_venta, p.ganancia, c.nombre_categoria, p.venta_mayoreo, p.inventariado, "
                    + "i.porcentaje AS iva, p.tipo_venta "
                    + "FROM productos p "
                    + "LEFT JOIN categorias c ON p.id_categoria = c.id_categoria "
                    + "LEFT JOIN iva i ON p.id_iva = i.id_iva";

            java.sql.Statement st = cn.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Object[] fila = {
                    rs.getInt("id_producto"),
                    rs.getString("codigo_barras"),
                    rs.getString("descripcion"),
                    rs.getDouble("precio_compra"),
                    rs.getDouble("precio_venta"),
                    rs.getDouble("ganancia"),
                    rs.getString("nombre_categoria"), // ahora muestra el nombre
                    rs.getDouble("venta_mayoreo"),
                    rs.getBoolean("inventariado"),
                    rs.getDouble("iva"), // porcentaje IVA
                    rs.getString("tipo_venta") // texto descriptivo (unidad, granel, paquete)
                };
                modelo.addRow(fila);
            }

            tablaModificar.setModel(modelo);

            // --- Agregar combos de selección en las columnas ---
            // Categoría
            javax.swing.JComboBox<String> comboCategoria = new javax.swing.JComboBox<>();
            cargarCategorias(comboCategoria);
            javax.swing.DefaultCellEditor editorCategoria = new javax.swing.DefaultCellEditor(comboCategoria);
            tablaModificar.getColumnModel().getColumn(6).setCellEditor(editorCategoria);

            // IVA
            javax.swing.JComboBox<String> comboIVA = new javax.swing.JComboBox<>();
            cargarIva(comboIVA);
            javax.swing.DefaultCellEditor editorIVA = new javax.swing.DefaultCellEditor(comboIVA);
            tablaModificar.getColumnModel().getColumn(9).setCellEditor(editorIVA);

            // Tipo de venta
            javax.swing.JComboBox<String> comboTipoVenta = new javax.swing.JComboBox<>();
            comboTipoVenta.addItem("unidad");
            comboTipoVenta.addItem("granel");
            comboTipoVenta.addItem("paquete");
            javax.swing.DefaultCellEditor editorTipoVenta = new javax.swing.DefaultCellEditor(comboTipoVenta);
            tablaModificar.getColumnModel().getColumn(10).setCellEditor(editorTipoVenta);
            // 🔄 NUEVO: Combo para Inventariado (true / false)
            javax.swing.JComboBox<String> comboInventariado = new javax.swing.JComboBox<>();
            comboInventariado.addItem("true");
            comboInventariado.addItem("false");
            javax.swing.DefaultCellEditor editorInventariado = new javax.swing.DefaultCellEditor(comboInventariado);
            tablaModificar.getColumnModel().getColumn(8).setCellEditor(editorInventariado);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al mostrar productos: " + e.getMessage());
            e.printStackTrace();
        } finally {
            con.desconectar();
        }
    }

