    private void inicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inicioActionPerformed
        String user = usuario.getText().trim();
    String pass = contraseña.getText().trim();

    if(user.isEmpty() || pass.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Debes llenar todos los campos");
        return;
    }

    con = new conectar();
    Connection cn = con.getConnection();

    try {
        // Consultar usuario y contraseña en la BD
        String sql = "SELECT tipo_usuario FROM usuarios WHERE usuario=? AND contraseña=?";
        java.sql.PreparedStatement pst = cn.prepareStatement(sql);
        pst.setString(1, user);
        pst.setString(2, pass);

        java.sql.ResultSet rs = pst.executeQuery();

        if(rs.next()) {
            String tipo = rs.getString("tipo_usuario");

            if(tipo.equals("admin")) {
                // Si es admin lo manda a otra ventana especial
                /*AdminPage admin = new AdminPage();
                admin.setVisible(true);*/
            } else {
                // Cualquier otro tipo lo manda al index
                index idx = new index();
                idx.setVisible(true);
            }

            // Cierra la ventana de login
            this.dispose();

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos");
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    } finally {
        con.desconectar();
    }
    }//GEN-LAST:event_inicioActionPerformed

