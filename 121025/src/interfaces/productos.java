    private void cargarIvaEnCombo() {
        conecxiones.conectar con = new conecxiones.conectar();
        java.sql.Connection cn = con.getConnection();
        java.sql.PreparedStatement ps = null;
        java.sql.ResultSet rs = null;

        try {
            ivacombox.removeAllItems(); // limpiar combo antes de cargar

            String sql = "SELECT id_iva, porcentaje FROM iva";
            ps = cn.prepareStatement(sql);
            rs = ps.executeQuery();

            boolean hay = false;
            java.text.DecimalFormat df = new java.text.DecimalFormat("0.00");

            while (rs.next()) {
                hay = true;
                int id = rs.getInt("id_iva");
                double porcentaje = rs.getDouble("porcentaje");
                String texto = df.format(porcentaje) + " %";

                ivacombox.addItem(new ComboItem(id, texto));
            }

            if (!hay) {
                JOptionPane.showMessageDialog(this, "No hay registros de IVA en la base de datos.", "Sin datos", JOptionPane.WARNING_MESSAGE);
            } else if (ivacombox.getItemCount() > 0) {
                ivacombox.setSelectedIndex(0); // seleccionamos el primero
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar IVAs: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            con.desconectar();
        }
    }

