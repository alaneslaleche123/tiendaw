    private void agregarDocumentListenerParaGanancia() {
        DocumentListener docListener = new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                calcularGanancia();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                calcularGanancia();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                calcularGanancia();
            }
        };

        precioCompra.getDocument().addDocumentListener(docListener);
        precioVenta.getDocument().addDocumentListener(docListener);
    }

// Método que calcula la ganancia
    private void calcularGanancia() {
        try {
            double venta = Double.parseDouble(precioVenta.getText());
            double compra = Double.parseDouble(precioCompra.getText());

            double resultado = venta - compra;
            sumaGlobal = resultado;

            // Mostrar con 2 decimales
            ganancia.setText(String.format("%.2f", resultado));
            // Cambiar color según si es negativa
            if (resultado < 0) {
                ganancia.setForeground(Color.RED);
            } else {
                ganancia.setForeground(Color.BLACK);
            }

        } catch (NumberFormatException e) {
            ganancia.setText("0.00"); // Valor por defecto si no hay números
            ganancia.setForeground(Color.BLACK);
        }
    }
