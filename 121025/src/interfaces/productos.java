    private void borrarModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarModificarActionPerformed
        // Verificar si hay una fila seleccionada
        int filaSeleccionada = tablaModificar.getSelectedRow();
        if (filaSeleccionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un producto para eliminar.");
            return;
        }

        // Confirmar acción
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "¿Está seguro de eliminar este producto?",
                "Confirmar eliminación",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return; // Si el usuario elige "No", no hace nada
        }

        // Obtener el ID del producto de la fila seleccionada
        int idProducto = Integer.parseInt(tablaModificar.getValueAt(filaSeleccionada, 0).toString());

        // Conexión a la base de datos
        conecxiones.conectar con = new conecxiones.conectar();
        java.sql.Connection cn = con.getConnection();
        java.sql.PreparedStatement psInventario = null;
        java.sql.PreparedStatement psProducto = null;

        try {
            // 🔹 1. Eliminar el producto del inventario (si existe)
            String sqlInventario = "DELETE FROM inventario WHERE id_producto = ?";
            psInventario = cn.prepareStatement(sqlInventario);
            psInventario.setInt(1, idProducto);
            psInventario.executeUpdate();

            // 🔹 2. Eliminar el producto de la tabla productos
            String sqlProducto = "DELETE FROM productos WHERE id_producto = ?";
            psProducto = cn.prepareStatement(sqlProducto);
            psProducto.setInt(1, idProducto);
            int resultado = psProducto.executeUpdate();

            if (resultado > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Producto eliminado correctamente.");
                mostrarProductos(); // Actualizar tabla después de eliminar
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo eliminar el producto (no encontrado).");
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar producto: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (psInventario != null) {
                    psInventario.close();
                }
                if (psProducto != null) {
                    psProducto.close();
                }
                con.desconectar();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_borrarModificarActionPerformed
